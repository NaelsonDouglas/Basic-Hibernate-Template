package administrative;



public class Course
{
	
	protected String name;

	
	protected Class classes;

	public Course(){
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Class getClasses() {
		return classes;
	}

	public void setClasses(Class classes) {
		this.classes = classes;
	}
	
}

